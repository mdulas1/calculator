# students grading system
score = int(input("enter your score here:"))
if score >= 70 and score <= 100 :
    print("A")
elif score >= 60 and score <= 69:
    print("B")
elif score >= 50 and score <= 59:
    print("C")
elif score >= 45 and score <= 49:
    print("D")
elif score <= 44:
    print("E")
else:
    print("invalid or out of range")
